#include <iostream>
using namespace std;
int main() {
int score;
cout << "Введіть оцінку студента (від 0 до 100): ";
cin >> score;

if (score < 0 || score > 100) {
    cout << "Помилка: Введена оцінка знаходиться поза допустимим діапазоном (0-100).\n";
    } 
// Використовуємо каскад else if для перевірки діапазонів від найбільшого до найменшого
    else if (score >= 90) {
// Діапазон 90-100
cout << "Ваша оцінка: Відмінно\n";
    } 
    else if (score >= 75) {
// Діапазон 75-89 (якщо оцінка не була >= 90, але >= 75)
cout << "Ваша оцінка: Добре\n";
    } 
    else if (score >= 60) {
// Діапазон 60-74
cout << "Ваша оцінка: Задовільно\n";
    } 
    else {
// Усі інші випадки (0-59)
cout << "Ваша оцінка: Незадовільно\n";
    }

    return 0;
}