#include <iostream>
#include <cmath> 
using namespace std;
int main() {
int X_initial; 
cout << "Завдання 3: Інкрементація та Декрементація\n";
cout << "Введіть ціле число X: ";
cin >> X_initial;
    int X; 
    int Y; 
// ДЕМОНСТРАЦІЯ ІНКРЕМЕНТАЦІЇ
cout << "\n1. Інкрементація (X++)\n";
// 1.1 Префіксна інкрементація
X = X_initial; 
cout << "Початкове X: " << X << endl;
cout << "Після префіксної (++X): " << ++X << endl; 
cout << "Кінцеве X: " << X << endl;
// Постфіксна інкрементація
X = X_initial;
cout << "\nПочаткове X: " << X << endl;
cout << "Після постфіксної (X++): " << X++ << endl;
cout << "Кінцеве X: " << X << endl;
// ДЕМОНСТРАЦІЯ ДЕКРЕМЕНТАЦІЇ
cout << "\n--- 2. Декрементація (X--) ---\n";
// Префіксна декрементація
 X = X_initial;
cout << "Початкове X: " << X << endl;
cout << "Після префіксної (--X): " << --X << endl; // X зменшується ПЕРЕД використанням
cout << "Кінцеве X: " << X << endl;
//Постфіксна декрементація
 X = X_initial;
cout << "\nПочаткове X: " << X << endl;
cout << "Після постфіксної (X--): " << X-- << endl; // X зменшується ПІСЛЯ використання
cout << "Кінцеве X: " << X << endl;
//ОБЧИСЛЕННЯ Y = 10 + X
cout << "\n--- 3. Обчислення Y = 10 + X ---\n";
// Y = 10 + X 
X = X_initial;
Y = 10 + (++X);
cout << "\nФормула: Y = 10 + (++X)";
cout << "\nРезультат: Y=" << Y << ", X=" << X << endl;
// Y = 10 + X з постфіксною інкрементацією (X++)
X = X_initial;
Y = 10 + (X++);
cout << "\nФормула: Y = 10 + (X++)";
cout << "\nРезультат: Y=" << Y << ", X=" << X << endl;

    return 0;
}